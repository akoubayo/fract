#include "fractol.h"

int	func_mm(int x, int y, t_env *e)
{
	x -= (600) / 2;
	y -= (600) / 2;
	if (e->mouse_g == 1)
	{
		e->x += x / e->zoom / 2.51;
		e->y -= y / e->zoom / 2.51;
		ft_fractol(e);
	}
	else if (e->julia == 0)
	{
		e->ca = ((float)x / 600) * 4;
		e->cb = ((float)y / 600) * 4;
		ft_fractol(e);
	}
	return (0);
}
